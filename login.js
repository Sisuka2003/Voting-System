
function usernameLoginClick() {
    var usernameSpan = document.getElementById("username");

    usernameSpan.className = ("un-span-top");
}

function usernameChangeLoginClick() {
    var usernameSpan = document.getElementById("username");

    usernameSpan.className = ("user-input-field");
}

